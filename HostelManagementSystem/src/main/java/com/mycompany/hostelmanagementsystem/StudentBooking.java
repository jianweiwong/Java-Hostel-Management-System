    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        if (acNo_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter account number");
        } else if (acNo_txt.getText().contains("|")) {
            JOptionPane.showMessageDialog(this, "Account number cannot contain the character '|'");
        } else {
            if (application.checkOngoingApplication(TP)) {
                JOptionPane.showMessageDialog(null, "Dear User: \n\n          "
                        + "You have PENDING or ACTIVE room aplication.    \n\n Please Try Again.", "Room Reserved", JOptionPane.WARNING_MESSAGE);
            } else {
                BookedYear = yearComboBox.getSelectedItem().toString();
                BookedMonth = monthComboBox.getSelectedItem().toString();
                String CheckIn = BookedMonth + "-" + BookedYear;
                String selectedDuration = DurationCB.getSelectedItem().toString();

                // Convert BookedYear, BookedMonth, and selectedDuration to integers
                int year = Integer.parseInt(BookedYear);
                int month = Integer.parseInt(BookedMonth);
                int RoomDuration = Integer.parseInt(selectedDuration);

                // Calculate the CheckOut year and month
                int checkOutYear = year + ((month + RoomDuration - 1) / 12);
                int checkOutMonth = (month + RoomDuration - 1) % 12 + 1;

                Random random = new Random();
                int randomNumber = random.nextInt(90000000) + 10000000;
                String refNo = String.valueOf(randomNumber);
                System.out.println(refNo);

                // Format the CheckOut date string
                String CheckOut = checkOutMonth + "-" + checkOutYear;
                System.out.println(TP + "|" + RoomNumber + "|" + CheckIn + "|" + CheckOut + "|" + RoomDuration + "|" 
                        + df.format(TotalRoomPrice) + "|" + "Pending" + "|" + refNo + "\n");
                String dataSubmit = (TP + "|" + RoomNumber + "|" + CheckIn + "|" + CheckOut + "|" + RoomDuration + "|" 
                        + df.format(TotalRoomPrice) + "|" + "Pending" + "|" + refNo + "\n");
                application.addApplication(dataSubmit);
                System.out.println(RoomNumber);
                roomStatus.modifyRoomStatus(RoomNumber, refNo,"Unavailable");
                exithere();
            }
        }
    }//GEN-LAST:event_submitActionPerformed

