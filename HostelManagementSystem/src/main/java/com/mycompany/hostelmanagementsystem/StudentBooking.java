    private void LoadRoomData() {

        ArrayList<String> SelectedRoomsDetails = new ArrayList<>();
        SelectedRoomsDetails = room.RoomDetails(RoomNumber);
        for (String roomDetails : SelectedRoomsDetails) {
            String[] roomData = roomDetails.split("\\|");
            // Extracting only the required columns (Room Type, Room Number, and Price)
            RoomNoPO.setText(roomData[1]);
            RoomNo.setText(roomData[1]);
            RoomType.setText(roomData[0]);
            RoomPrice.setText(roomData[2]);
            RoomPricePO.setText(roomData[2]);
            RoomDesc.setText(roomData[3]);
            RoomPriceMonth = Double.parseDouble(roomData[2]);
        }

    }

