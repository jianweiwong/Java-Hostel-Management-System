    private void updateDaysComboBox(javax.swing.JComboBox<String> cmbDay,
            javax.swing.JComboBox<String> cmbMonth,
            javax.swing.JComboBox<String> cmbYear) {
        String selectedMonth = (String) cmbMonth.getSelectedItem();
        int selectedYear = Integer.parseInt((String) cmbYear.getSelectedItem());
        int daysInMonth;

        switch (selectedMonth) {
            case "1":
            case "3":
            case "5":
            case "7":
            case "8":
            case "10":
            case "12":
                daysInMonth = 31;
                break;
            case "4":
            case "6":
            case "9":
            case "11":
                daysInMonth = 30;
                break;
            case "2":
                daysInMonth = isLeapYear(selectedYear) ? 29 : 28;
                break;
            default:
                daysInMonth = 0;
                break;
        }

        cmbDay.removeAllItems();
        for (int day = 1; day <= daysInMonth; day++) {
            cmbDay.addItem(String.valueOf(day));
        }
    }

    private boolean isLeapYear(int year) {
        return year % 400 == 0 || (year % 4 == 0 && year % 100 != 0);
    }


