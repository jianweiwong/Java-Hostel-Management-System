    public double generateMonthlyIncomeReport(DefaultTableModel model) {
        model.setRowCount(0); // Clear existing data
        double totalIncome = 0.0;

        try {
            BufferedReader reader = new BufferedReader(new FileReader("application.txt"));
            String line;
            double[] monthlyIncome = new double[12];

            while ((line = reader.readLine()) != null) {
                String[] applicationData = line.split("\\|");

                // Check if the AdminApplication status is "Accepted"
                if (applicationData[6].equals("Accepted") || applicationData[6].equals("CheckOut")) {
                    String startMonth = applicationData[2];
                    String price = applicationData[5];
                    double income = Double.parseDouble(price);

                    // Extract the month from the startMonth string
                    String monthString = startMonth.split("-")[0];
                    int month = Integer.parseInt(monthString);

                    // Calculate the index in the monthlyIncome array
                    int index = month - 1;

                    // Update the monthly income
                    monthlyIncome[index] += income;

                    // Update the total income
                    totalIncome += income;
                }
            }

            // Populate the jTable with the monthly income data
            DateFormatSymbols dateFormatSymbols = new DateFormatSymbols();
            String[] months = dateFormatSymbols.getMonths();

            for (int i = 0; i < 12; i++) {
                String monthName = months[i];
                double income = monthlyIncome[i];

                // Format the income value as a double
                String formattedIncome = String.format("%.2f", income);

                model.addRow(new Object[]{monthName, formattedIncome});
            }

            reader.close();
        } catch (IOException e) {
            e.printStackTrace();
        }

        return totalIncome;
    }

    public static void generateRoomListReport(JTable table) {
        // Step 1: Read data from the file and store in a data structure
        HashMap<String, List<String>> roomData = new HashMap<>();
        try {
            BufferedReader reader = new BufferedReader(new FileReader("roomStatus.txt"));
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split("\\|");
                String roomType = getRoomType(parts[0]);
                String roomStatus = parts[1];

                // Add AdminRoom status to the list for the respective AdminRoom type
                List<String> roomStatusList = roomData.getOrDefault(roomType, new ArrayList<>());
                roomStatusList.add(roomStatus);
                roomData.put(roomType, roomStatusList);
            }
            reader.close();
        } catch (IOException e) {
            e.printStackTrace();
            // Handle file read error
        }

        // Step 2: Initialize counters
        int totalStandardRooms = 0;
        int totalDeluxeRooms = 0;
        int totalSuiteRooms = 0;
        int availableStandardRooms = 0;
        int availableDeluxeRooms = 0;
        int availableSuiteRooms = 0;

        // Step 3: Count the total and available rooms per type
        if (roomData.containsKey("Standard")) {
            List<String> standardRooms = roomData.get("Standard");
            totalStandardRooms = standardRooms.size();
            availableStandardRooms = countAvailableRooms(standardRooms);
        }

        if (roomData.containsKey("Deluxe")) {
            List<String> deluxeRooms = roomData.get("Deluxe");
            totalDeluxeRooms = deluxeRooms.size();
            availableDeluxeRooms = countAvailableRooms(deluxeRooms);
        }

        if (roomData.containsKey("Suite")) {
            List<String> suiteRooms = roomData.get("Suite");
            totalSuiteRooms = suiteRooms.size();
            availableSuiteRooms = countAvailableRooms(suiteRooms);
        }

        // Step 4: Update the jTable with the generated report
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0); // Clear existing data

        // Add row for Standard rooms
        model.addRow(new Object[]{"Standard", "500.00", totalStandardRooms, availableStandardRooms});

        // Add row for Deluxe rooms
        model.addRow(new Object[]{"Deluxe", "700.00", totalDeluxeRooms, availableDeluxeRooms});

        // Add row for Suite rooms
        model.addRow(new Object[]{"Suite", "900.00", totalSuiteRooms, availableSuiteRooms});
    }

    private static String getRoomType(String roomCode) {
        if (roomCode.startsWith("A")) {
            return "Standard";
        } else if (roomCode.startsWith("B")) {
            return "Deluxe";
        } else if (roomCode.startsWith("C")) {
            return "Suite";
        } else {
            return "";
        }
    }

    private static int countAvailableRooms(List<String> roomStatusList) {
        int count = 0;
        for (String roomStatus : roomStatusList) {
            if (roomStatus.equals("Available")) {
                count++;
            }
        }
        return count;
    }

